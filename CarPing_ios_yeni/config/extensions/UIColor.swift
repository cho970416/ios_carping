import UIKit

extension UIColor {
  @nonobjc class var greyish: UIColor {
    return UIColor(white: 167.0 / 255.0, alpha: 1.0)
  }
    
    @nonobjc class var teal: UIColor {
      return UIColor(red: 1.0 / 255.0, green: 153.0 / 255.0, blue: 131.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var shamrockGreen: UIColor {
      return UIColor(red: 3.0 / 255.0, green: 191.0 / 255.0, blue: 104.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var greenBlue: UIColor {
        return UIColor(red: 2.0 / 255.0, green: 179 / 255.0, blue: 112 / 255.0, alpha: 1.0)
    }
}
